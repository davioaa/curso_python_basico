# fatiamento = [inicio:fim:oasso] # passo = de quanto em quanto ira pular a exibição
# funcao len retorna a quantidade de caracteres da string

var = 'Olá mundo'

print(var[0]) # exibe apenas o indice escolhido
print(var[0:5]) # exibe do indice 0 ao 5
print(var[1:]) # exibe da parte definida até o final da string
print(var[::2]) # pula de 2 em 2
print(var[::-1]) # inverte a string

print(len(var)) # quantidade de caracteres